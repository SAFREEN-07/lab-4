public class arithmeticsubpackage {
     public void add(int x,int y)
    {
       System.out.println(ad(x,y));
    }
    public int ad(int x,int y)
    {
        return x+y;
    }
     public void sub(int x,int y)
    {
       System.out.println(sb(x,y));
    }
    public int sb(int x,int y)
    {
        return x-y;
    }
     public void mul(int x,int y)
    {
       System.out.println(mulp(x,y));
    }
    public int mulp(int x,int y)
    {
        return x*y;
    }
     public void div(int x,int y)
    {
       System.out.println(di(x,y));
    }
    public int di(int x,int y)
    {
        return x/y;
    }
     public void mod(int x,int y)
    {
       System.out.println(md(x,y));
    }
    public int md(int x,int y)
    {
        return x%y;
    }
    
    
}
